package com.apache.map;

/**
 *
 * @author Faris <https://github.com/faris-mckay>
 */
public class Location {

    public Location(int x, int y) {
        this.x = x;
        this.y = y;
    }

    private int x;

    private int y;

    /**
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(int y) {
        this.y = y;
    }

}
